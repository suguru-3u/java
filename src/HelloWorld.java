public class HelloWorld {
    public static void main(String[] args){
        // 標準出力
        System.out.println("Hello Word");
        System.out.println("My name is s.ohira");

        // 数字の変数(型によって格納できる数が異なる)
        int age = 28;

        // 真偽値
        boolean isJapan = true;

        // 一文字を格納
        char first = 'a';

        // 文字列を格納
        String name = "suguru";

        // 定数
        final String fullName = "ohira suguru";
    }
}
